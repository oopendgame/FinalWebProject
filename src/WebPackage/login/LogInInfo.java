package WebPackage.login;

public class LogInInfo {
	private int id;
	private String userName;
	
	public LogInInfo() {
		
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public int getId() {
		return id;
	}
	
	public String getUserName() {
		return userName;
	}

}
